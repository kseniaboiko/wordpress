            <h2>2007 рік</h2>
            <a class="mceItemAnchor" name="f2975">
                <p>Держбюджетна НДР №2975-ф. „Методи та засоби паралельного скорочення моделей надвеликих RLC схем”</p>
            </a>
            <p>Науковий керівник роботи – д.т.н., професор Петренко А.І.</p>
            <p>Відповідальний виконавець – к.т.н., доцент Ладогубець В.В.</p>
            <p>Здійснено аналіз структур великих інтегральних схем з метою виявлення впливу взаємного розташування елементів електронної схеми та впливу міжз’єднань. Цей вплив урахований шляхом включення до структури проектуємої схеми еквівалентних схем заміщення ліній зв’язку у вигляді паразитних RLC ланцюгів. Виявлені особливості конфігурації та міжз’єднань та оцінені практичні розміри паразитних RLC ланцюгів.</p>
            <p>Розроблені рекомендації та вимоги до особливостей конфігурацій та міжз’єднань надвеликих RLC кіл для швидкодіючих ВІС та МЕМС.</p>
            <p>Розроблені та досліджені нові підходи та методи скорочення моделей паразитних RLC ланцюгів, які базуються як на ідеях макромоделювання, так і на використанні Y-∆ перетворювання. Проведені дослідження особливостей алгоритмів зберігання, пошуку та перетворювання&nbsp; інформації&nbsp; у надвеликих масивах даних. З урахуванням розміру задач (до декілька мільйонів компонентів) розроблені відповідні обчислювальні алгоритми, які реалізовані&nbsp; у вигляді паралельних процедур, до складу яких входять спеціально розроблені ефективні та швидкодіючі&nbsp;&nbsp; алгоритми зберігання, пошуку та перетворювання інформації у надвеликих масивах даних.</p>
            <p>Розроблені алгоритми скорочення надвеликих RC схем в основу яких закладені наступні положення:</p>
            <ul>
                <li>Попереднє упорядкування усіх вузлів схеми по параметру RC.</li>
                <li>Визначення всіх вузлів для яких RC « ω<sub>б </sub>, де ω<sub>б</sub> – кругова базова частота функціонування пристрою.</li>
                <li>Почергове скорочення за допомогою Y-∆ перетворювання вузлів, визначених на попередньому етапі.</li>
            </ul>
            <p>Розроблені алгоритми скорочення паразитних RC ланцюгів на базі матричних методів макромоделювання.</p>
            <p>Розраховані відповідні співвідношення для виконання процедури скорочення та проведено їх перевірку на модельних задачах. Теоретично доведено адекватність та точність розрахованих співвідношень. Розроблено алгоритм, який поширює цей підхід на випадок RLC ланцюгів.</p>

            <a class="mceItemAnchor" name="ndr107">
                <p>Госпдоговірна НДР № 1-07. “Розробка системи підвищення пропускної спроможності оптоволоконних ліній зв’язку із застосуванням широкополосних частотних мультиплексорів”</p>
            </a>

            <p>Науковий керівник роботи- к.т.н., доц. Артюхов В.Г.,</p>
            <p>Проведено аналіз існуючих технологій підвищення пропускної спроможності оптоволоконних ліній зв’язку. Показано, що використання широкополосних частотних мультиплексорів дозволяє найбільш ефективно підвищити пропускну спроможність оптоволоконних ліній зв’язку. Розроблено структуру системи з використанням широкополосних частотних мультиплексорів. Розроблено програмне забезпечення керування системою. Проведено комплексні дослідження апаратури в лабораторних умовах, показано, що розроблена система відповідає світовому рівню. Показано, що впровадження системи дозволяє в деяких випадках зовсім відказатися від будівництва нових оптоволоконних ліній зв’язку, а тільки прозвести заміну приємно-передаючого обладнання, що коштує на порядок дешевше.</p>
            <p>Результати НДР запропоновані до впровадження операторам зв’язку, операторам інтернет, операторам кабельного телебачення.</p>

