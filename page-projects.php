            <h2>2006 рік</h2>

            <a class="mceItemAnchor" name="ndr106">
                <p>Госпдоговірна НДР № 1-06 „Розробка системи системи комутації оптоволоконих ліній зв’язку з захистом від несанкціонованого доступу”.</p>
            </a>

            <p>Науковий керівник роботи – к.т.н., доцент Артюхов В.Г.</p>
            <p>Відповідальний виконавець – н.с. Стацюра Є.В.</p>
            <p>Аналіз телекомунікаційних мереж загального користування, а також мереж кабельного телебачення засвідчує, що сьогодні різко зростає кількість волокон у оптоволоконних кабелів зв’язку. У сьогоденний час кількість оптичних волокон може бути більш 544 і цей показник постійно зростає. Крім того до оптичних волокон при монтажі та комутації не можна торкатися, тому що це призводить до появи перехідної перешкоди тривалістю 0,3 сек., що при швидкості передачі 622 Мбіт/c призводить до втрати до 200 Мбіт інформації.</p>
            <p>Пропонується система комутації оптичних волокон з захистом від несанкціонованого доступу, яка дозволяє керувати більш ніж 4000 оптичних волокон оптоволоконних ліній зв’язку з мінімізацією перехідної перешкоди, яка призводить до збоїв в роботі приладів передачі даних. Система реалізує неможливість доступу до працюючих волокон по яких реалізується передача даних. Комутація можлива тільки не працюючих волокон.</p>
            <p>Результати впровадження системи комутації оптоволоконних ліній зв’язку з захистом від несанкціонованого доступу дозволяють комутувати оптичні волокна, без втрати інформації, зменшити час переходу на резервні волокна, збільшити перешкодозахищеність оптоволоконних ліній зв’язку.</p>

            <a class="mceItemAnchor" name="Panasonic">
                <p>Panasonic</p>
            </a>
            <p>Через Інноваційний бізнес-інкубатор «Політехцентр», який є складовою частиною технополіса «Київська політехніка», виконується проект від 01.04.2006 з Лабораторією цифрових телекомунікацій компанії PANASONIC. Відділення Північної Америки Panasonic, Принстон, Нью-Джерсі, 08540, США. Область використання результатів Проекту відноситься до професійних телекомунікаційних і мережевих технологій.</p>

