    <h2>Конкурсний бал</h2>
    <p> розраховується за формулою: КБ = П1 + П2 + RA,   де</p>
    <ul>
        <li>П1 –результат ЗНО з іноземної мови (за шкалою від 100 до 200 балів), </li>
        <li>П2 –оцінка фахового вступного випробування (за шкалою від 100 до 200 балів),</li>
        <li> RA = 2RA + 2RT,</li>
        <li>	
        RA – академічна складова в п’ятибальній системі,(95-100 (A)–5 балів; 
        85-94 (B)–4,5 балів;75-84 (C)–4 бали;65-74 (D)–3,5 балів;
        60-64 (E)–3 бали;31-59 –2,5 бали;1-30–2 бали). </li>
        <li>RT – складова творчих досягнень (не більше ніж 5 балів)</li></ul>
