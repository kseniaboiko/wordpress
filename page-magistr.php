        <h2>Блок дисциплін за вибором "Комп’ютерні розподілені системи проектування"</h2>
        <p><b>Інформаційні технології проектування – </b>галузь комп’ютерних наук, що пов’язана з комп'ютеризацією творчої інженерної діяльності людини  і створюванням апаратно-програмних комп’ютерних систем або чисто програмних великих комплексів різного призначення для застосуванням на всіх етапах <b>індивідуального чи колективного оптимізаційного мережевого проектування </b> нових об'єктів і технологій штучного середовища. Фахівці цієї спеціальності є по сутті<b> “системними інтеграторами”</b>. Фахівці з цієї спеціальності готуються <b>для науково-дослідної, проектної і організаційно-управлінської діяльності в галузі застосування сучасних інформаційних технологій</b> для інформатизації роботи установ і організацій , проектування найрізноманітніших об’єктів техніки і штучного оточення людини. Основні напрями майбутньої діяльності:</p>
        <ul>
            <li><b>розроблення нових шляхів використання комп'ютерів </b>(електронна комерція, мультимедійні системи, медичні діагностичні центри, пошукові інформаційні системи, автоматизація проектування, вбудовані системи)</li>
            <li> <b>розроблення дієвих шляхів рішення задач комп'ютеризації</b>(алгоритми і програми математичних доказів і висновків, символьних обчислень, алгоритмів паралельної обробки інформації, інструментарію паралельного програмування, розпізнавання образів і зображень, віртуалізації ресурсів, семантичного пошуку і інше)</li>
            <li><b>створення, упровадження і використання прикладного і системного програмного забезпечення </b><b> для різних областей наукової, інженерної і бізнес діяльності</b>: (розробка Веб – сервісів, прикладного програмного забезпечення, засобів безпеки даних, комп'ютерної графіки, ігор, мережних мультимедіа інформаційних ресурсів, систем автоматизованого проектування (САПР), корпоративних інформаційних систем ERP (Enterprise Resource Planning) і т.д.)</li>
            <li><b>планування і реалізація інформаційної інфраструктури організації і управління нею</b> (розгортання і конфігурація комп'ютерних мереж, управління обчислювальним середовищем, інформаційними системами, базами даних, програмним забезпеченням для розподілених систем та інше).</li>
            <li><b>автоматизоване проектування великих та надвеликих інтегральних схем </b>(використання в інформаційних технологіях сучасних досягнень мікро і наноелетроніки).</li></ul>
        <p>Комплекс дисциплін по вивченню електроніки, обробки сигналів , оптимізації та досвід викладачів з розробки систем автоматизації проектування дозволяє кафедрі успішно готувати фахівців в галузі проектування інтегральних схем. Широкі міжнародні зв’язки кафедри забезпечують підготовку з урахуванням останніх досягнень галузі з використанням сучасних промислових стандартів проектування інтегрованих в системі CADENCE.</p>
