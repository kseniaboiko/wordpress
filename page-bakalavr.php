        <p><b>«Інформаційні системи і технології проектування»</b> спрямована на створення і застосування комп’ютерних систем на базі сучасних ІТ для автоматизації організаційної та інженерної діяльності підприємств і комп’ютерного проектування різноманітних об’єктів техніки та штучного оточення людини: побудова інформаційних систем та баз даних, створення програмного забезпечення для розподілених інформаційних систем, конфігурація обчислювальної інфраструктури організації з використанням сучасних Інтернет – технологій і управління нею, використання ІТ для наскрізного проектування нових елементів інформаційних систем та їх елементної бази, для управління бізнесом.</p>
        <p><b>«Системне проектування сервісів»</b> що спрямована на створення, налаштування і моделювання систем сервісів різноманітного призначення: функціональних, інфраструктурних, управлінських. Це перспективний напрямок системної архітектури, що набуває все більшої популярності і швидко розвивається. Він орієнтований на надання кінцевому користувачу програмних, інформаційних, апаратних ресурсів як завершених послуг, шляхом застосування спеціальних методів побудови програмного забезпечення, використання сучасних Інтернет-технологій, методів та технологій аналізу надвеликих обсягів даних, технологій управління документообігом та управління проектами. </p>
        <p>При складанні навчальних планів і програм окремих дисциплін основна увага була приділена наступних <b>напрямах підготовки:</b></p>
        <p><b>Підготовка з програмування</b> включає розвиток алгоритмічного мислення, засвоєння основних етапів і технологій розробки ПЗ, розповсюджених мов програмування: С, С++, C#, Java, PHP, JS, Python, HTML5 та інші, засобів розробки ПЗ та програмних продуктів: Visual Studio, Eclipse, NetBeans, Intelij IDEA, QT Creator, MPI, Open MP, Wolphram Mathematica, Mathlab, MySQL, Workbech, Active HDL, MaxPlus, Cadence, Allted та ін. Студенти вивчають технології прикладного, системного і мережевого програмування. </p>
        <p><b>Математична підготовка</b> необхідна для створення моделей об'єктів і процесів інформатизації, які відрізняються різноманітністю використовуваних методів. Тому програма спрямована як на посилену загальну математичну підготовку, так і на засвоєння таких прикладних розділів як обчислювальна математика, математичне програмування, розподілені обчислення, обчислювальна геометрія, методи штучного інтелекту та інтелектуальної обробки великих масивів даних.</p>
        <p><b>Технічна підготовка</b> спрямована на отримання базових знань в області апаратних засобів сучасних інформаційних систем, а також комп'ютерних і телекомунікаційних мереж (включаючи такі новітні технології як GRID, «хмарні» і суперкомп'ютерні).</p>
        <p><b>Системна підготовка</b> з одного боку спрямована на вивчення системного і стандартного прикладного програмного забезпеченні (ПЗ комп'ютерних мереж, реляційних і об'єктно-орієнтованих СУБД, засобів захисту інформації, мультимедійних застосувань і т.д.). а з іншого – на опанування методів проектування комп'ютерних систем (від великих інтегральних схем до розподілених інформаційних систем, Web-порталів, тощо).</p>
        <p><a class = "allmode_link" href="/uk/navchalnij-proces/navchalnij-plan" mce_href="/uk/navchalnij-proces/navchalnij-plan">Перелік дисциплін навчального плану</a><br mce_bogus="1"></p>
        <p>З метою посилення <b>практичної комп'ютерної підготовки</b> майбутніх фахівців майже всі навчальні курси (навіть більшість математичних) підкріплено лабораторними практикумами, які проводяться на базі різноманітних прикладних систем автоматизації: обчислень, програмування, проектування, графіки, документообігу &nbsp;та ін. Студенти також мають можливість ще під час навчання брати участь в реалізації інноваційних проектів, працюючи у спільних з партнерськими фірмами Melexis (Бельгія) і Miratech (Україна) науково-навчальних лабораторіях, в центрі суперкомп'ютерних обчислень НТУУ КПІ, центрі розвитку і підтримки сайтів КПІ, отримати досвід супроводу і адміністрування потужної комп’ютерної мережі кафедри СП</p>
