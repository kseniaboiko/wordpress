        <h2>Чому - Комп’ютерні науки ?</h2>
        <p>Ми всі живемо у глобальному інформаційному просторі. Сьогодні кількома кліками на клавіатурі комп’ютера чи мобільного пристрою незалежно від часу та місця перебування можна замовити покупки і сплатити рахунки, здійснити аудіо і відео спілкування, отримати будь яку інформацію та комп’ютерні розваги. Це стало вже звичним. Всесвітнє павутиння, швидко розвиваючись, пропонує все нові і нові послуги (сервіси), надає у спільне використання тисячам індивідуальних і професійних користувачів таке програмне забезпечення і апаратні мережеві ресурси (хмарні сервіси), які для кожного з них окремо є економічно невиправданими чи взагалі недоступними. Набуває все більшої популярності Інтернет речей (ІоТ), коли комп’ютеризовані пристрої спілкуються один з одним без втручання людини, керуючи побутовою технікою, безпілотними апаратами, поливом рослин в теплицях та ін. На вимогу таких систем створюються принципово нові технології, методи оброблення даних і програмування, що постійно розвиваються.</p>
        <p>Для того, щоб створювати, обслуговувати, застосовувати в роботі підприємств і організацій такі сервіси потрібні спеціалісти, що володіють знаннями та уміннями, які надають саме <b>«Комп’ютерні науки»</b>.</p>
        <p>В області комп'ютерних наук зазвичай виділяють наступні <b>основні розділи</b>: </p>
        <ul><li>алгоритми і структури даних, </li><li>мови програмування, </li><li>архітектура комп'ютерів,</li><li>операційні системи і комп'ютерні мережі, </li><li>розробка програмного забезпечення, </li><li>бази даних і інформаційно-пошукові системи, <br></li><li>штучний інтелект, </li><li>комп'ютерна графіка, </li><li>інтерфейс людини і інформаційної системи та ін.</li></ul>
        <p><b>Метою</b> навчання комп'ютерним наукам є підготовка фахівців, що володіють комплексними знаннями в області програмного та апаратного забезпечення і чітко розуміють їхню взаємодію. Стати фахівцем з сучасних інформаційних технологій (ІТ) можливо тільки за умови комплексного вивчення всіх складових частин комп'ютерних наук.</p>
        <p><b>Методи підготовки</b> кафедри СП спрямовані на формування у фахівця системного мислення, широкого кругозору у сфері новітніх інформаційних технологій і методів програмування, навичок і умінь, спрямованих на створення власних, експлуатацію відомих і самостійне опанування новими програмно-апаратними комп'ютерними системами. Такого фахівця можна назвати системний архітектор (інтегратор).</p>
        <h2 id="readMoreBachelor">Чи потрібна вища освіта для роботи в ІТ?</h2>
        <p>Кому з нас не доводилося читати на Інтернет форумах або самому брати участь в гарячих суперечках на цю тему? При цьому досить авторитетні представники кожної зі сторін дуже переконливо і часто занадто емоційно наводять аргументи на користь своєї точки зору.</p>
        <p>А корінь питання швидше криється в звичайному консерватизмі нашого мислення. На відміну від інших галузей, таких як машинобудування або будівництво, ІТ розвивалися просто вибуховими темпами. На очах одного покоління зі сфери діяльності вчених ентузіастів ІТ перетворилися в потужну індустрію. І як в будь-якій індустрії в ІТ потрібні свої вчені, інженери, техніки, технологи, робітники.</p>
        <p>Інженерів і наукові кадри готують, як відомо, ВНЗ, кадри середньої ланки - середні спеціальні заклади, для виконання робочих операцій - досить закінчити відповідні комп'ютерні курси.</p>
        <p>Плутанину в цю картину вносить відсутність  в ІТ стандартної кваліфікаційної сітки, яка є в старих галузях промисловості, таких як хімічна або машинобудування. Певне уявлення про посадові обов'язки може дати, наприклад, «<a class = "allmode_link" href="http://cad.kpi.ua/uk/vstup/pracevlashtuvannja" >Типові посади працівників ІТ–галузі» </a>. Чому певне? Тому, що трактування кваліфікаційних вимог ще залежить від самої компанії – її масштабу і типу виконуваних проектів. Кому не відомо, що за посадою офіс менеджера можуть стояти обов'язки секретарки, що розносить каву на нарадах, диспетчера або секретаря-референта. Різниця, погодьтеся, чимала. А ось оголошення, що просто вразило своєю «креативністю»: Запрошуємо на роботу ЕКСПЕРТІВ з доставки піци.</p>
        <p><br></p>
        <p>Додаткову плутанину в питання вносить успішне існування невеликих приватних організацій і вільних підприємців, які займаються розробкою сайтів-візиток, комп'ютерним дизайном, ремонтом комп'ютерів, пишуть мобільні додатки і т.д. Природно, що для таких робіт знання математичних методів або хмарних технологій зайве. Але і для роботи в автосервісі, або у бригаді по ремонту квартир знання з опору матеріалів теж не потрібні. Однак ніхто не дискутує на тему, а чи потрібні вони в машинобудуванні або будівництві.</p>
